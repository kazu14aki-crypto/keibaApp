/** "1:32.0" のようなタイム文字列を秒数（float）に変換する */
export function timeStrToSeconds(timeStr) {
  if (!timeStr) return null;
  const m = timeStr.match(/^(\d+):(\d{2})\.(\d)$/);
  if (!m) return null;
  return parseInt(m[1], 10) * 60 + parseInt(m[2], 10) + parseInt(m[3], 10) / 10;
}

/** 馬場状態による簡易タイム補正（秒）。良馬場を基準(0)とし、稍重以降は基準タイムを遅く見積もる。 */
function conditionAdjustmentSec(condition, distance) {
  const distFactor = (distance || 1800) / 1800; // 距離が長いほど影響も大きいと仮定した単純な比例補正
  switch (condition) {
    case '稍重': return 0.5 * distFactor;
    case '重': return 1.2 * distFactor;
    case '不良': return 2.0 * distFactor;
    default: return 0;
  }
}

/**
 * 1走分の走破タイムを評価する。
 * JRAレーティング（重賞のみ付与される、そのレースの絶対的な強さを示す指数。
 * 100前後を平均的な強さの基準とし、高いほど強いレースだったことを示す）が
 * 取得できている場合はそれを最優先で使う（基準タイムDBより精度が高いため）。
 * レーティングがない場合は、競馬場・距離・クラス別の基準タイムとの比較にフォールバックする。
 *
 * 戻り値: { diffSec, level, hasData, source }
 *   diffSec: 基準タイムとの差（秒）。レーティング使用時は擬似的な秒換算値。
 *   level: 'excellent' | 'good' | 'average' | 'below' のいずれか（参考表示用）
 *   source: 'rating' | 'base_time' のいずれか（どちらの方式で評価したか）
 */
export function evaluateRaceTime(race) {
  if (!race) return { hasData: false, reason: 'データなし' };

  // レーティングが取得できている場合は最優先で使う（重賞のみ）。
  // レーティング100を基準とし、10ポイント differenceを概ね0.5秒相当として疑似的に扱う。
  if (race.rating && race.rating > 0) {
    const ratingDiff = race.rating - 100;
    const pseudoDiffSec = ratingDiff / 20; // 20ポイント差で約1秒相当の目安
    let level = 'average';
    if (ratingDiff >= 8) level = 'excellent';
    else if (ratingDiff >= 2) level = 'good';
    else if (ratingDiff <= -8) level = 'below';
    return {
      hasData: true,
      diffSec: Math.round(pseudoDiffSec * 10) / 10,
      level,
      source: 'rating',
      rating: race.rating,
    };
  }

  if (!race.time || !race.track || !race.surface || !race.distance) {
    return { hasData: false, reason: 'タイム・競馬場・距離のいずれかが不明' };
  }

  const seconds = timeStrToSeconds(race.time);
  if (seconds === null) return { hasData: false, reason: 'タイム形式を解釈できません' };

  const tier = inferClassTier(race.class_name);
  // class_nameが不明な場合はOP相当として評価（JRAページでレース名が取得できなかったケース）
  const effectiveTier = tier || 'OP';
  const tierNote = tier ? '' : '（クラス不明のためOP相当で推定）';

  const key = `${race.track}_${race.surface}_${race.distance}`;
  const table = BASE_TIMES[key];
  if (!table || table[effectiveTier] === undefined) {
    return { hasData: false, reason: `基準タイム未収録（${key}）` };
  }

  const baseTime = table[effectiveTier];
  const adjustedBase = baseTime + conditionAdjustmentSec(race.condition, race.distance);
  const diffSec = adjustedBase - seconds; // 正なら基準より速い

  let level = 'average';
  if (diffSec >= 1.0) level = 'excellent';
  else if (diffSec >= 0.3) level = 'good';
  else if (diffSec <= -1.0) level = 'below';

  return { hasData: true, diffSec: Math.round(diffSec * 10) / 10, level, tier: effectiveTier, baseTime: adjustedBase, source: 'base_time', tierNote };
}

/**
 * 過去4走すべてを評価し、タイム指数（0〜20点）として集約する。
 * 直近のレースほど重みを大きくする（前走>前々走>3走前>4走前）。
 */
export function calcTimeIndexFromHistory(history) {
  const labels = ['前走', '前々走', '3走前', '4走前'];
  const weights = [0.4, 0.3, 0.2, 0.1];

  let weightedSum = 0;
  let weightTotal = 0;
  const details = [];

  labels.forEach((label, i) => {
    const race = history?.[label];
    if (!race) return;
    const evalResult = evaluateRaceTime(race);
    details.push({ label, ...evalResult });
    if (evalResult.hasData) {
      weightedSum += evalResult.diffSec * weights[i];
      weightTotal += weights[i];
    }
  });

  if (weightTotal === 0) {
    return { score: 10, hasData: false, reason: '評価可能な過去走タイムなし（中立値）', details };
  }

  const avgDiff = weightedSum / weightTotal;
  // diffSec: -2秒〜+2秒程度を想定し、0〜20点にマッピング（中央10点）
  const score = Math.max(0, Math.min(20, Math.round(10 + avgDiff * 5)));

  return {
    score,
    hasData: true,
    reason: `加重平均タイム差 ${avgDiff > 0 ? '+' : ''}${avgDiff.toFixed(2)}秒（基準タイムとの比較）`,
    details,
  };
}
